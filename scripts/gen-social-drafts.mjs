#!/usr/bin/env node
/**
 * gen-social-drafts.mjs — Generate a 5-channel social draft template for
 * a published MDX article, with UTM links pre-filled.
 *
 * Wraps `./utm-builder.mjs` for UTM construction. Reads frontmatter from
 * `src/content/posts/{slug}.mdx` and emits a markdown file at
 * `docs/social/{slug}.md` with placeholders the writer (Sofia or a downstream
 * AI pass) fills in. Aligned with:
 *   - sofia/docs/utm-convention.md (medium matrix)
 *   - .omni/harness/plans.md §4.1 D0-D7 + §4.2 channel requirements
 *   - sofia/docs/blog-publishing-workflow.md §6.3 PR body template
 *   - sofia/docs/voice-brief.md §2 / §4 (style constraints surfaced to writer)
 *
 * Usage:
 *   node scripts/gen-social-drafts.mjs --slug {slug}
 *   node scripts/gen-social-drafts.mjs --slug zero-to-ai-native \
 *     --campaign pillar-zero-to-ai-native \
 *     --audience ai-workers \
 *     --output docs/social/zero-to-ai-native.md
 *
 * Programmatic:
 *   import { generateSocialDraftsForSlug } from "./gen-social-drafts.mjs";
 *
 * Exit codes:
 *   0 — success
 *   1 — generation error (MDX not found, bad frontmatter, UTM build error)
 *   2 — usage error (missing required flag)
 *   3 — self-test failure
 *   4 — output file exists and --force not set
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import {
  buildArticleSocialUtms,
  articleCanonicalUrl,
  validateCampaign,
} from "./utm-builder.mjs";
import { parseFrontmatter } from "./mdx-nid-coverage.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = resolve(__dirname, "..");
const DEFAULT_POSTS_DIR = resolve(REPO_ROOT, "src", "content", "posts");
const DEFAULT_OUTPUT_DIR = resolve(REPO_ROOT, "docs", "social");

const SUPPORTED_AUDIENCES = new Set([
  "ai-workers",
  "humanities-readers",
  "travel-life",
  "mixed",
]);

const VOICE_CONSTRAINTS = [
  "觸發點開場（voice-brief §2.1）— 從具體場景、對話、數字、照片切入",
  "反轉式 pivot 中段（voice-brief §2.3）— 「我才意識到 / 後來才發現」",
  "結尾留旅程感（voice-brief §2.6）— 不寫「總而言之 / 綜上所述」",
  "禁用語（voice-brief §4）— 不用中國網路用語、AI/PR 套話、step-by-step playbook、販賣焦慮",
];

// ----- Helpers ------------------------------------------------------------

function todayIso() {
  const d = new Date();
  return d.toISOString().slice(0, 10);
}

function deriveDateFromPublishedAt(publishedAt) {
  if (!publishedAt) return null;
  // Accept "YYYY-MM-DD", "YYYY-MM-DDTHH:MM:SS+08:00", or ISO with Z.
  const m = publishedAt.match(/^(\d{4}-\d{2}-\d{2})/);
  return m ? m[1] : null;
}

function deriveDefaultCampaign(slug) {
  return `pillar-${slug}`;
}

function readArticleFrontmatter(slug, postsDir) {
  const path = join(postsDir, `${slug}.mdx`);
  const fallback = join(postsDir, `${slug}.md`);
  let actual = null;
  if (existsSync(path)) actual = path;
  else if (existsSync(fallback)) actual = fallback;
  if (!actual) {
    throw new Error(`MDX not found for slug "${slug}": looked at ${path} and ${fallback}`);
  }
  const text = readFileSync(actual, "utf-8");
  const fm = parseFrontmatter(text);
  if (!fm) throw new Error(`no frontmatter found in ${actual}`);
  return { path: actual, frontmatter: fm };
}

function channelByKey(bundle, key) {
  const ch = bundle.find((c) => c.key === key);
  if (!ch) throw new Error(`bundle missing channel "${key}"`);
  return ch;
}

// ----- Renderer -----------------------------------------------------------

function renderTemplate({ slug, frontmatter, campaign, audience, bundle, generatedAt }) {
  const d0 = channelByKey(bundle, "d0-main");
  const d1 = channelByKey(bundle, "d1-x");
  const d3 = channelByKey(bundle, "d3-story");
  const d5 = channelByKey(bundle, "d5-why");
  const d7 = channelByKey(bundle, "d7-recap");
  const canonical = articleCanonicalUrl(slug);
  const title = frontmatter.title || slug;
  const excerpt = frontmatter.excerpt || "";

  // For English summary, reuse D0 main URL but tag content as `-d0-en`.
  // We rebuild it inline to avoid mutating the canonical channel definition.
  const enContentTag = `${(frontmatter.publishedAt || generatedAt).slice(0, 10)}-${slug}-d0-en`;

  const lines = [];
  lines.push(`# Social Drafts — ${title}`);
  lines.push("");
  lines.push(`> Generated by \`scripts/gen-social-drafts.mjs\` on ${generatedAt}`);
  lines.push(`> Source MDX: \`sofia/src/content/posts/${slug}.mdx\``);
  lines.push(`> Canonical: ${canonical}`);
  lines.push(`> UTM campaign: \`${campaign}\``);
  lines.push(`> Audience: \`${audience}\``);
  lines.push(`> Voice brief: \`sofia/docs/voice-brief.md\` — writer MUST honour §2 / §4 / §7`);
  lines.push(`>`);
  lines.push(`> Placeholders use \`{{TODO_*}}\`. Replace before posting. Do NOT alter UTM links.`);
  lines.push("");

  // Voice constraints summary
  lines.push(`## ✦ Voice constraints to honour while filling`);
  lines.push("");
  for (const c of VOICE_CONSTRAINTS) {
    lines.push(`- ${c}`);
  }
  lines.push("");

  // Quick UTM index
  lines.push(`## ✦ UTM Quick Index`);
  lines.push("");
  lines.push(`| Day | Channel | UTM URL |`);
  lines.push(`|---|---|---|`);
  lines.push(`| D0 | LinkedIn long  | ${d0.url} |`);
  lines.push(`| D1 | X / Twitter    | ${d1.url} |`);
  lines.push(`| D3 | Threads / FB   | ${d3.url} |`);
  lines.push(`| D5 | LinkedIn follow-up | ${d5.url} |`);
  lines.push(`| D7 | Z App recap    | ${d7.url} _(no UTM by design)_ |`);
  lines.push(`| EN | LinkedIn EN    | ${canonicalWithEnUtm(canonical, campaign, enContentTag)} |`);
  lines.push("");

  // D0 LinkedIn long
  lines.push(`## D0 — LinkedIn long-form (600-1200 字, reflective)`);
  lines.push("");
  lines.push(`Channel: \`linkedin-lf\`  ·  Post Tue/Wed/Thu 08:30-10:00 Asia/Taipei`);
  lines.push("");
  lines.push("```text");
  lines.push(`{{TODO_LINKEDIN_LONG_INTRO_HOOK — 一段觸發點：對話 / 數字 / 場景，不開定義 (≤ 3 句)}}`);
  lines.push("");
  lines.push(`{{TODO_LINKEDIN_LONG_BODY — 中段反轉 pivot，引用 ${title} 的核心觀察 (200-600 字)}}`);
  lines.push("");
  lines.push(`{{TODO_LINKEDIN_LONG_OPEN_ENDING — 留問題或下一段旅程感 (1-2 句)}}`);
  lines.push("");
  lines.push(`完整文章：${d0.url}`);
  lines.push("```");
  lines.push("");

  // D1 X / Twitter
  lines.push(`## D1 — X / Twitter (≤280 chars OR 3-post thread)`);
  lines.push("");
  lines.push(`Channel: \`x\`  ·  Post 12:00-13:30 or 21:00-22:30 Asia/Taipei`);
  lines.push("");
  lines.push(`### Option A — Single post`);
  lines.push("");
  lines.push("```text");
  lines.push(`{{TODO_X_SINGLE_OBSERVATION — 一句核心觀察 + 留白；中文約 70 字內含 URL}}`);
  lines.push("");
  lines.push(`${d1.url}`);
  lines.push("```");
  lines.push("");
  lines.push(`### Option B — 3-post thread`);
  lines.push("");
  lines.push("```text");
  lines.push(`1/ {{TODO_X_T1_TRIGGER — 觸發點}}`);
  lines.push("");
  lines.push(`2/ {{TODO_X_T2_PIVOT — 反轉觀察}}`);
  lines.push("");
  lines.push(`3/ {{TODO_X_T3_QUESTION — 留問題}}`);
  lines.push(`   ${d1.url}`);
  lines.push("```");
  lines.push("");

  // D3 Threads / Facebook
  lines.push(`## D3 — Threads / Facebook (story-first)`);
  lines.push("");
  lines.push(`Channel: \`fb-personal\`  ·  Post 21:00-23:00 Asia/Taipei`);
  lines.push("");
  lines.push(`> Note: Threads-native medium is **pending** per \`utm-convention.md\` + \`plans.md\` §4.3.`);
  lines.push(`> Until convention is updated, cross-post via Sofia's Facebook personal page (\`fb-personal\`).`);
  lines.push("");
  lines.push("```text");
  lines.push(`{{TODO_FB_STORY_OPEN_SCENE — 一個場景 / 一張照片 / 一個對話片段}}`);
  lines.push("");
  lines.push(`{{TODO_FB_STORY_MID_OBSERVATION — 從場景帶出的觀察 (≤ 250 字)}}`);
  lines.push("");
  lines.push(`{{TODO_FB_STORY_EXIT_RESIDUE — 留下情緒或問題餘味}}`);
  lines.push("");
  lines.push(`→ ${d3.url}`);
  lines.push("```");
  lines.push("");

  // D5 LinkedIn follow-up
  lines.push(`## D5 — LinkedIn follow-up「我寫這篇的原因」`);
  lines.push("");
  lines.push(`Channel: \`linkedin-lf\`  ·  Post 08:30-10:00 or 17:30-18:30 Asia/Taipei`);
  lines.push("");
  lines.push("```text");
  lines.push(`{{TODO_LINKEDIN_FOLLOWUP_WHY_I_WROTE — 自述為什麼寫這篇 / 哪一段最猶豫 / 收到什麼回饋 (200-500 字)}}`);
  lines.push("");
  lines.push(`原文：${d5.url}`);
  lines.push("```");
  lines.push("");

  // D7 Z App recap
  lines.push(`## D7 — Z App internal recap (no public UTM)`);
  lines.push("");
  lines.push(`Channel: \`z-app\` (Z minutes comment / post; no public UTM by design)`);
  lines.push("");
  lines.push("```text");
  lines.push(`{{TODO_Z_RECAP_KPI_SNAPSHOT — 從 weekly_traffic_report 拿 D0-D7 真實 / pending KPI；明列哪幾項是 _pending_}}`);
  lines.push("");
  lines.push(`{{TODO_Z_RECAP_NEXT_PROMOTION_SUGGESTION — 下一波推廣建議：哪段話可二次截圖 / 哪個 quote 適合再延伸成 X thread}}`);
  lines.push("");
  lines.push(`Canonical: ${d7.url}`);
  lines.push("```");
  lines.push("");

  // EN summary
  lines.push(`## EN — English summary (rewritten, not direct translation)`);
  lines.push("");
  lines.push(`Channel: \`linkedin-lf\`  ·  Same canonical, separate UTM (\`utm_content\` ends with \`-d0-en\`)`);
  lines.push("");
  lines.push("```text");
  lines.push(`{{TODO_EN_SUMMARY_REWRITTEN — 不直譯。重寫成英文讀者的 hook + 觀察 + 留白 (400-800 字)}}`);
  lines.push("");
  lines.push(`Read in English-leaning post: ${canonicalWithEnUtm(canonical, campaign, enContentTag)}`);
  lines.push("```");
  lines.push("");

  // Generation metadata
  lines.push(`## Generation metadata`);
  lines.push("");
  lines.push(`- Generator: \`scripts/gen-social-drafts.mjs\` v0`);
  lines.push(`- Slug: \`${slug}\``);
  lines.push(`- Generated at: ${generatedAt}`);
  lines.push(`- Source frontmatter:`);
  lines.push(`  - title: ${title}`);
  if (excerpt) lines.push(`  - excerpt: ${excerpt}`);
  if (frontmatter.publishedAt) lines.push(`  - publishedAt: ${frontmatter.publishedAt}`);
  if (frontmatter.category) lines.push(`  - category: ${frontmatter.category}`);
  lines.push(`- UTM bundle (5-channel JSON):`);
  lines.push("```json");
  lines.push(JSON.stringify(bundle, null, 2));
  lines.push("```");

  return lines.join("\n") + "\n";
}

function canonicalWithEnUtm(canonical, campaign, contentTag) {
  const u = new URL(canonical);
  u.searchParams.set("utm_source", "sofia-blog");
  u.searchParams.set("utm_medium", "linkedin-lf");
  u.searchParams.set("utm_campaign", campaign);
  u.searchParams.set("utm_content", contentTag);
  return u.toString();
}

// ----- Public API ----------------------------------------------------------

export function generateSocialDraftsForSlug({
  slug,
  postsDir = DEFAULT_POSTS_DIR,
  campaign,
  audience = "mixed",
  date,
  generatedAt,
}) {
  if (!slug) throw new Error("slug is required");
  if (!SUPPORTED_AUDIENCES.has(audience)) {
    throw new Error(
      `unsupported audience "${audience}"; must be one of ${[...SUPPORTED_AUDIENCES].join(", ")}`
    );
  }

  const { path, frontmatter } = readArticleFrontmatter(slug, postsDir);
  const effectiveDate = date || deriveDateFromPublishedAt(frontmatter.publishedAt) || todayIso();
  const effectiveCampaign = campaign || deriveDefaultCampaign(slug);
  const cErr = validateCampaign(effectiveCampaign);
  if (cErr) throw new Error(`invalid campaign "${effectiveCampaign}": ${cErr}`);

  const bundle = buildArticleSocialUtms({
    slug,
    date: effectiveDate,
    campaign: effectiveCampaign,
  });

  const generatedAtStr = generatedAt || new Date().toISOString();
  const markdown = renderTemplate({
    slug,
    frontmatter,
    campaign: effectiveCampaign,
    audience,
    bundle,
    generatedAt: generatedAtStr,
  });

  return {
    slug,
    sourcePath: path,
    frontmatter,
    campaign: effectiveCampaign,
    audience,
    bundle,
    markdown,
  };
}

// ----- CLI -----------------------------------------------------------------

function parseArgs(argv) {
  const args = {};
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--stdout")    { args.stdout = true; continue; }
    if (a === "--force")     { args.force = true; continue; }
    if (a === "--self-test") { args.selfTest = true; continue; }
    if (a === "--help" || a === "-h") { args.help = true; continue; }
    if (a.startsWith("--")) {
      const key = a.slice(2);
      const v = argv[i + 1];
      if (v === undefined || v.startsWith("--")) {
        return { _error: `flag --${key} requires a value` };
      }
      args[key] = v;
      i += 1;
      continue;
    }
    return { _error: `unknown argument "${a}"` };
  }
  return args;
}

const HELP = `\
gen-social-drafts — produce a 5-channel social draft template for an article

Flags:
  --slug <slug>         required (matches src/content/posts/{slug}.mdx)
  --campaign <camp>     default: pillar-{slug}
  --audience <a>        ai-workers | humanities-readers | travel-life | mixed (default mixed)
  --date <YYYY-MM-DD>   default: derived from publishedAt frontmatter, else today
  --output <path>       default: docs/social/{slug}.md
  --posts-dir <path>    default: src/content/posts/
  --stdout              print to stdout instead of writing file
  --force               overwrite existing output file
  --self-test           run internal unit tests
  --help / -h           this message
`;

function runSelfTest() {
  let pass = 0, fail = 0;
  function expect(name, fn) {
    try { fn(); pass++; process.stdout.write(`  ✓ ${name}\n`); }
    catch (e) { fail++; process.stdout.write(`  ✗ ${name}\n    ${e.message}\n`); }
  }
  function assertTrue(v, m) { if (!v) throw new Error(m || "expected truthy"); }
  function assertEq(a, b, m) { if (a !== b) throw new Error(`${m||""} expected ${JSON.stringify(b)} got ${JSON.stringify(a)}`); }
  function assertContains(s, sub, m) { if (typeof s !== "string" || !s.includes(sub)) throw new Error(`${m||""} expected "${sub}" in output`); }

  process.stdout.write("gen-social-drafts self-test\n");

  expect("generates for existing zero-to-ai-native slug", () => {
    const r = generateSocialDraftsForSlug({
      slug: "zero-to-ai-native",
      generatedAt: "2026-05-29T12:00:00Z",
    });
    assertEq(r.slug, "zero-to-ai-native");
    assertEq(r.campaign, "pillar-zero-to-ai-native");
    assertEq(r.bundle.length, 5);
    assertTrue(r.markdown.length > 1000, "markdown should be substantial");
  });

  expect("markdown contains all 5 channel sections", () => {
    const r = generateSocialDraftsForSlug({
      slug: "zero-to-ai-native",
      generatedAt: "2026-05-29T12:00:00Z",
    });
    assertContains(r.markdown, "## D0 — LinkedIn long-form");
    assertContains(r.markdown, "## D1 — X / Twitter");
    assertContains(r.markdown, "## D3 — Threads / Facebook");
    assertContains(r.markdown, "## D5 — LinkedIn follow-up");
    assertContains(r.markdown, "## D7 — Z App internal recap");
    assertContains(r.markdown, "## EN — English summary");
  });

  expect("UTM URLs in output use sofia-blog source and registered mediums", () => {
    const r = generateSocialDraftsForSlug({
      slug: "zero-to-ai-native",
      generatedAt: "2026-05-29T12:00:00Z",
    });
    assertContains(r.markdown, "utm_source=sofia-blog");
    assertContains(r.markdown, "utm_medium=linkedin-lf");
    assertContains(r.markdown, "utm_medium=x");
    assertContains(r.markdown, "utm_medium=fb-personal");
    // D7 is canonical without UTM by design — check that the bundle d7 has no UTM
    const d7 = r.bundle.find((c) => c.key === "d7-recap");
    assertEq(d7.medium, null);
  });

  expect("voice constraints surfaced to writer", () => {
    const r = generateSocialDraftsForSlug({
      slug: "zero-to-ai-native",
      generatedAt: "2026-05-29T12:00:00Z",
    });
    assertContains(r.markdown, "觸發點開場");
    assertContains(r.markdown, "反轉式 pivot");
    assertContains(r.markdown, "結尾留旅程感");
    assertContains(r.markdown, "禁用語");
  });

  expect("placeholders use {{TODO_*}} convention", () => {
    const r = generateSocialDraftsForSlug({
      slug: "zero-to-ai-native",
      generatedAt: "2026-05-29T12:00:00Z",
    });
    const matches = r.markdown.match(/\{\{TODO_[A-Z0-9_]+/g) || [];
    assertTrue(matches.length >= 8, `expected ≥8 TODO placeholders, got ${matches.length}`);
  });

  expect("date derived from publishedAt frontmatter", () => {
    const r = generateSocialDraftsForSlug({
      slug: "zero-to-ai-native",
      generatedAt: "2026-05-29T12:00:00Z",
    });
    const d0 = r.bundle.find((c) => c.key === "d0-main");
    // zero-to-ai-native publishedAt is 2026-05-21
    assertContains(d0.utm_content, "2026-05-21");
  });

  expect("EN summary URL has utm_content ending with -d0-en", () => {
    const r = generateSocialDraftsForSlug({
      slug: "zero-to-ai-native",
      generatedAt: "2026-05-29T12:00:00Z",
    });
    assertContains(r.markdown, "-d0-en");
  });

  expect("rejects unknown audience", () => {
    let threw = false;
    try {
      generateSocialDraftsForSlug({ slug: "zero-to-ai-native", audience: "nonsense" });
    } catch (e) { threw = /unsupported audience/.test(e.message); }
    assertTrue(threw, "should throw on bad audience");
  });

  expect("rejects nonexistent slug", () => {
    let threw = false;
    try {
      generateSocialDraftsForSlug({ slug: "this-slug-does-not-exist" });
    } catch (e) { threw = /MDX not found/.test(e.message); }
    assertTrue(threw, "should throw when MDX missing");
  });

  expect("custom campaign respected", () => {
    const r = generateSocialDraftsForSlug({
      slug: "zero-to-ai-native",
      campaign: "speaker-q2-2026",
      generatedAt: "2026-05-29T12:00:00Z",
    });
    assertContains(r.markdown, "utm_campaign=speaker-q2-2026");
  });

  expect("metadata block includes bundle JSON", () => {
    const r = generateSocialDraftsForSlug({
      slug: "zero-to-ai-native",
      generatedAt: "2026-05-29T12:00:00Z",
    });
    assertContains(r.markdown, "## Generation metadata");
    assertContains(r.markdown, '"key": "d0-main"');
  });

  process.stdout.write(`\n${pass} passed, ${fail} failed\n`);
  return fail === 0;
}

async function runCli(argv) {
  const args = parseArgs(argv);
  if (args._error) { process.stderr.write(`error: ${args._error}\n${HELP}`); return 2; }
  if (args.help)   { process.stdout.write(HELP); return 0; }
  if (args.selfTest) return runSelfTest() ? 0 : 3;

  if (!args.slug) {
    process.stderr.write(`error: --slug is required\n${HELP}`);
    return 2;
  }
  let result;
  try {
    result = generateSocialDraftsForSlug({
      slug: args.slug,
      postsDir: args["posts-dir"] ? resolve(args["posts-dir"]) : undefined,
      campaign: args.campaign,
      audience: args.audience || "mixed",
      date: args.date,
    });
  } catch (e) {
    process.stderr.write(`error: ${e.message}\n`);
    return 1;
  }

  if (args.stdout) {
    process.stdout.write(result.markdown);
    return 0;
  }

  const outputPath = args.output
    ? resolve(args.output)
    : join(DEFAULT_OUTPUT_DIR, `${args.slug}.md`);

  if (existsSync(outputPath) && !args.force) {
    process.stderr.write(
      `error: ${outputPath} already exists. Use --force to overwrite, or --stdout to preview.\n`
    );
    return 4;
  }

  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, result.markdown);
  process.stdout.write(`wrote ${outputPath} (${result.markdown.length} bytes)\n`);
  return 0;
}

const isMain = import.meta.url === `file://${process.argv[1]}`;
if (isMain) {
  runCli(process.argv.slice(2)).then((c) => process.exit(c ?? 0));
}
